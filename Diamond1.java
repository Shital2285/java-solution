
import java.util.*;
import java.io.*;

class Pyramid1
{
  public static void main(String args[])
  {
	 Scanner sc=new Scanner(System.in);
	 int row;
	 int i,j;
	 System.out.println("enter the rows");
	 row=sc.nextInt();
	 for(i=0;i<row;i++)
	 {
		 for (j=0;j<row;j++)
		 {
			 System.out.println("* ");
			 
		 }
	 }
		 
	 
	 
 
  }

}