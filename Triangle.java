import java.util.*;

import java.io.*;

class Triangle
{
  public static void main(String args[])
{
  int i,j,n;
   Scanner sc=new Scanner(System.in);
   System.out.println(" Enter the number");
   n=sc.nextInt();
   for(i=0; i<n; i++)
   {
    System.out.print(" * ");
    for(j=1; j<=i; j++)
    {
    System.out.print(" * ");
    }
     System.out.println("");
            
  }

}

}
























